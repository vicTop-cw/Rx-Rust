print("ignore_elements:", Observable.from_iter([1, 2, 3]).ignore_elements().collect())
print("element_at(0):", Observable.from_iter([10, 20, 30]).element_at(0).collect())
print("take_last(2):", Observable.from_iter([1, 2, 3, 4, 5]).take_last(2).collect())
print("skip_last(2):", Observable.from_iter([1, 2, 3, 4, 5]).skip_last(2).collect())
