print("bottom_k(2):", Observable.from_iter([5, 3, 1, 4, 2]).bottom_k(2).collect())
print("switch_map iter:", Observable.from_iter([1, 2]).switch_map(lambda x: [x, x * 10]).collect())
print("combine_latest:", Observable.from_iter([1, 2, 3]).combine_latest([10, 20], lambda a, b: a + b).collect())
print("abs:", Observable.from_iter([-3, 0, 5]).abs().collect())
